import math


def whostheclosest(s, queries):
    # result array
    intarr = []
    slen = len(s)

    # loop through the queries
    for charidx in queries:

        # sanity check
        if charidx >= slen:
            intarr.append(-1)
            continue

        tchar = s[charidx]
        left = -1
        right = -1

        # traverse both directions
        lidx = charidx - 1
        ridx = charidx + 1

        while (True):
            if lidx >= 0:
                if s[lidx] == tchar:
                    left = lidx
                    break
                lidx -= 1
            if ridx <= slen - 1:
                if s[ridx] == tchar:
                    right = ridx
                    break
                ridx += 1
            if lidx <= 0 and ridx >= slen - 1:
                break

        # check and add to the list
        if left == -1:
            intarr.append(right)
        elif right == -1:
            intarr.append(left)
        else:
            if right - charidx < charidx - left:
                intarr.append(right)
            else:
                intarr.append(left)

    # when the loop is done return the int array
    return intarr


def whostheclosest2(s, queries):
    # results array
    intarr = []

    # make a dictionary for the string chars and map {char : [idxs]}
    charset = dict()
    for charidx in range(len(s)):
        char = s[charidx]
        try:
            charset[char].append(charidx)
        except KeyError:
            charset.setdefault(char, [charidx])

    # loop through the queries and
    for qloc in queries:
        try:
            # the array of locations for a char
            locs = charset[s[qloc]]
            llen = len(locs)
            # if only the query location exists in charset[char] then append -1
            if llen <= 1:
                intarr.append(-1)
            # if it's at the beginning add the next
            elif locs[0] == qloc:
                intarr.append(locs[1])
            # if it's at the end add the prev
            elif locs[-1] == qloc:
                intarr.append(locs[-2])
            # if it's in the middle test the min difference
            else:
                # binary search for the query start location for the char
                strt = 0
                stop = llen - 1
                idx = 0
                while strt <= stop:
                    idx = (stop+strt) // 2
                    if qloc == locs[idx]:
                        break
                    if qloc > locs[idx]:
                        strt = idx
                    else:
                        stop = idx
                strtloc = idx
                if abs(locs[strtloc] - locs[strtloc - 1]) <= abs(locs[strtloc] - locs[strtloc + 1]):
                    intarr.append(locs[strtloc - 1])
                else:
                    intarr.append(locs[strtloc + 1])

        except KeyError:
            print("something me doing")

    # when the loop is done return an int array of results
    return intarr


